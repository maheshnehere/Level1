package com.chatgpt.arrayproblem;

public class Table {
    //Java Program to Print Multiplication Table
    public static void main(String[] args) {
        int num = 5;
        int result = 0;

        for (int i =1;i<=10;i++)
        {
           result =   num*i;
            System.out.println("5"+"*"+i +"="+result);

        }

    }
}
